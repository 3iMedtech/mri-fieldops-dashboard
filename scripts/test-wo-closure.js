/**
 * Deep test: create a WO, drive it to in_progress, then exercise the
 * closure modal end-to-end (Issue 3) and the Sub-WO button (Issue 6).
 */
const { chromium } = require('playwright');
const URL = 'https://3imedtech.github.io/mri-fieldops-dashboard/staging/';
const ADMIN = { email: 'abhijit.s@3imedtech.com', pass: 'Shiva@23S' };
const ENG   = { email: 'engineer@3imedtech.com',  pass: 'Shiva@23S' };
const sleep = ms => new Promise(r => setTimeout(r, ms));

async function login(page, user) {
  await page.goto(URL, { waitUntil: 'domcontentloaded' });
  await page.waitForSelector('#login-email', { timeout: 15000 });
  await page.fill('#login-email', user.email);
  await page.fill('#login-password', user.pass);
  await page.click('button[type="submit"]');
  const start = Date.now();
  while (Date.now() - start < 15000) {
    const cls = await page.evaluate(() => document.body.className).catch(() => '');
    if (cls && (cls.includes('superadmin-mode') || cls.includes('viewer-mode') || cls.includes('manager-mode'))) return;
    await sleep(400);
  }
}

(async () => {
  const browser = await chromium.launch({ headless: true });
  const ctx = await browser.newContext();
  const page = await ctx.newPage();
  const errors = [];
  page.on('pageerror', e => errors.push(`pageerror: ${e.message}`));
  const log = [];
  const ok = m => log.push('PASS ' + m);
  const bad = m => log.push('FAIL ' + m);

  try {
    await login(page, ADMIN);
    await sleep(2500);

    // Create a new WO via the modal so we have a known WO id.
    await page.evaluate(() => navigate('app-tickets'));
    await sleep(800);
    await page.click('button:has-text("New Work Order")');
    await sleep(500);
    // pick first asset
    const assetOpts = await page.$$eval('#nt-asset option', os => os.map(o => o.value).filter(Boolean));
    if (!assetOpts.length) { bad('No assets to pick'); throw new Error('aborted'); }
    await page.selectOption('#nt-asset', assetOpts[0]);
    await sleep(250);
    await page.selectOption('#nt-call-type', 'Incident');
    await sleep(200);
    // sys-status auto-fills for Incident → 'down' or 'partially_down'
    await page.selectOption('#nt-sys-status', 'down');
    // assign first available engineer
    const engOpts = await page.$$eval('#nt-assigned-to option', os => os.map(o => o.value).filter(Boolean));
    if (engOpts.length) await page.selectOption('#nt-assigned-to', engOpts[0]);
    await page.fill('#nt-issue', 'WO closure test (auto-generated by test-wo-closure.js)');
    await page.click('#nt-submit');
    await sleep(2500);

    // Grab the newly created WO id (it should be the top row in the open list)
    const newId = await page.$eval('#at-tbody tr:first-child td:first-child', td => td.textContent.trim());
    log.push(`· Created WO: ${newId}`);

    // Move to in_progress via API state machine
    await page.evaluate(id => _openTicketDetail(id), newId);
    await sleep(700);
    await page.click('#atd-transitions button:has-text("Start Work")');
    await sleep(1500);

    // Re-open detail, now Mark Resolved button should be present
    await page.evaluate(id => _openTicketDetail(id), newId);
    await sleep(700);
    const resBtnVisible = await page.$('#atd-transitions button:has-text("Mark Resolved")');
    if (!resBtnVisible) { bad('Mark Resolved not visible after Start Work'); throw new Error('aborted'); }
    ok('Mark Resolved visible after in_progress');

    // Sub-WO button must be visible (Issue 6, on open primary WO)
    const subVis = await page.$eval('#atd-create-sub-btn', b => b && b.offsetParent !== null).catch(() => false);
    if (subVis) ok('Sub-WO create button visible on in_progress primary WO');
    else bad('Sub-WO create button NOT visible on in_progress primary WO');

    // Click Mark Resolved → closure modal must open
    await resBtnVisible.click();
    await sleep(700);
    const csState = await page.evaluate(() => {
      const m = document.getElementById('close-status-modal');
      return { hidden: m.getAttribute('aria-hidden'), classList: m.className };
    });
    const isOpen = csState.hidden === 'false' || csState.classList.includes('active');
    if (isOpen) ok(`Closure modal opens (aria-hidden=${csState.hidden})`);
    else bad(`Closure modal did not open (aria-hidden=${csState.hidden}, cls=${csState.classList})`);

    // Select "Working" and confirm
    await page.check('#close-status-modal input[value="working"]');
    await sleep(150);
    await page.click('#cs-confirm');
    await sleep(2500);

    // Verify DB state
    const dbState = await page.evaluate(id => {
      const t = APP_TICKETS.find(x => x.id === id);
      return t ? { status: t.status, sys_status: t.sys_status } : null;
    }, newId);
    if (dbState && dbState.status === 'resolved' && dbState.sys_status === 'working') {
      ok(`WO state after closure: status=${dbState.status}, sys_status=${dbState.sys_status}`);
    } else {
      bad(`Unexpected WO state after closure: ${JSON.stringify(dbState)}`);
    }

    // Now test sub-WO creation
    await page.evaluate(id => _openTicketDetail(id), newId);
    await sleep(500);
    // Note: WO is now resolved, sub-WO button should be hidden (not open).
    // Let's re-open in_progress to test sub-WO. Move via Reopen.
    const reopenBtn = await page.$('#atd-transitions button:has-text("Reopen")');
    if (reopenBtn) {
      await reopenBtn.click();
      await sleep(1500);
      await page.evaluate(id => _openTicketDetail(id), newId);
      await sleep(500);
      const subBtn = await page.$('#atd-create-sub-btn:visible');
      if (subBtn) {
        ok('Sub-WO button visible after reopen to in_progress');
        await subBtn.click();
        await sleep(500);
        const subModalOpen = await page.evaluate(() => {
          const m = document.getElementById('sub-wo-modal');
          return m && m.getAttribute('aria-hidden') === 'false';
        });
        if (subModalOpen) ok('Sub-WO modal opens');
        else bad('Sub-WO modal failed to open');
        await page.click('#sub-wo-modal button:has-text("Cancel")');
        await sleep(300);
      } else {
        bad('Sub-WO button hidden after reopen');
      }
    }

    if (errors.length) bad(`Page errors: ${errors.slice(0,3).join(' | ')}`);
    else ok('No JS errors during deep test');
  } catch (e) {
    bad(`Exception: ${e.message}`);
  } finally {
    await browser.close();
  }

  console.log(log.join('\n'));
  const fails = log.filter(l => l.startsWith('FAIL'));
  process.exit(fails.length ? 1 : 0);
})();
